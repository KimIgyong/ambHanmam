// 1. ExpenseNumberService
// ═════════════════════════════════════════════════════════════════════════════
describe('ExpenseNumberService', () => {
  let service: ExpenseNumberService;
  let repo: ReturnType<typeof createMockRepository>;

  beforeEach(async () => {
    repo = createMockRepository();

    const module: TestingModule = await Test.createTestingModule({
      providers: [
        ExpenseNumberService,
        { provide: getRepositoryToken(ExpenseRequestEntity), useValue: repo },
      ],
    }).compile();

    service = module.get(ExpenseNumberService);
  });

  describe('generateNumber', () => {
    it('첫 번째 채번은 EXP-YYYYMM-0001 형식이어야 한다', async () => {
      const qb = createMockQueryBuilder();
      qb.getCount.mockResolvedValue(0);
      repo.createQueryBuilder.mockReturnValue(qb);

      const result = await service.generateNumber('ent-1', '2026-03-10');
      expect(result).toBe('EXP-202603-0001');
    });

    it('기존 3건이 있으면 EXP-YYYYMM-0004 가 반환되어야 한다', async () => {
      const qb = createMockQueryBuilder();
      qb.getCount.mockResolvedValue(3);
      repo.createQueryBuilder.mockReturnValue(qb);

      const result = await service.generateNumber('ent-1', '2026-03-15');
      expect(result).toBe('EXP-202603-0004');
    });

    it('월이 바뀌면 별도 시퀀스로 채번되어야 한다', async () => {
      const qb = createMockQueryBuilder();
      qb.getCount.mockResolvedValue(0);
      repo.createQueryBuilder.mockReturnValue(qb);

      const result = await service.generateNumber('ent-1', '2026-04-01');
      expect(result).toBe('EXP-202604-0001');
    });
  });
});

