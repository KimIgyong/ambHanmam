// 2. ExpenseRequestService.findAll
// ═════════════════════════════════════════════════════════════════════════════
describe('ExpenseRequestService - findAll', () => {
  let service: ExpenseRequestService;
  let repo: ReturnType<typeof createMockRepository>;

  const mockDataSource = {
    transaction: jest.fn((cb: any) => cb({ create: jest.fn(), save: jest.fn() })),
  };

  beforeEach(async () => {
    repo = createMockRepository();

    const module: TestingModule = await Test.createTestingModule({
      providers: [
        ExpenseRequestService,
        { provide: getRepositoryToken(ExpenseRequestEntity), useValue: repo },
        { provide: getRepositoryToken(ExpenseRequestItemEntity), useValue: createMockRepository() },
        { provide: getRepositoryToken(ExpenseAttachmentEntity), useValue: createMockRepository() },
        { provide: getDataSourceToken(), useValue: mockDataSource },
        {
          provide: ExpenseNumberService,
          useValue: { generateNumber: jest.fn().mockResolvedValue('EXP-202603-0001') },
        },
      ],
    }).compile();

    service = module.get(ExpenseRequestService);
  });

  it('MANAGER 권한은 전체 목록을 조회한다 — andWhere(uid) 조건 미적용', async () => {
    const qb = createMockQueryBuilder();
    qb.getManyAndCount.mockResolvedValue([[], 0]);
    repo.createQueryBuilder.mockReturnValue(qb);

    await service.findAll('ent-1', mockUser('MANAGER'), {});

    // MANAGER(roleLevel=3)는 userId 필터 없이 조회
    const andWhereCalls = qb.andWhere.mock.calls.map((c: any) => c[0] as string);
    const hasUserFilter = andWhereCalls.some((q: string) => q.includes('exrApprover1Id'));
    expect(hasUserFilter).toBe(false);
  });

  it('MEMBER 권한은 자신이 작성/승인자인 건만 조회한다 — andWhere(uid) 조건 적용', async () => {
    const qb = createMockQueryBuilder();
    qb.getManyAndCount.mockResolvedValue([[], 0]);
    repo.createQueryBuilder.mockReturnValue(qb);

    await service.findAll('ent-1', mockUser('MEMBER'), {});

    const andWhereCalls = qb.andWhere.mock.calls.map((c: any) => c[0] as string);
    const hasUserFilter = andWhereCalls.some((q: string) => q.includes('exrApprover1Id'));
    expect(hasUserFilter).toBe(true);
  });

  it('페이지네이션 파라미터가 올바르게 적용되어야 한다', async () => {
    const qb = createMockQueryBuilder();
    qb.getManyAndCount.mockResolvedValue([[makeExpense()], 1]);
    repo.createQueryBuilder.mockReturnValue(qb);

    const result = await service.findAll('ent-1', mockUser('MASTER'), { page: '2', limit: '10' } as any);

    expect(result.page).toBe(2);
    expect(result.limit).toBe(10);
    expect(qb.skip).toHaveBeenCalledWith(10);
    expect(qb.take).toHaveBeenCalledWith(10);
  });
});

