// 3. ExpenseApprovalService
// ═════════════════════════════════════════════════════════════════════════════
describe('ExpenseApprovalService', () => {
  let service: ExpenseApprovalService;
  let requestRepo: ReturnType<typeof createMockRepository>;
  let approvalRepo: ReturnType<typeof createMockRepository>;
  let numberService: { generateNumber: jest.Mock };
  let mailService: ReturnType<typeof createMockMailService>;

  beforeEach(async () => {
    requestRepo = createMockRepository();
    approvalRepo = createMockRepository();
    numberService = { generateNumber: jest.fn().mockResolvedValue('EXP-202603-0001') };
    mailService = createMockMailService();

    const module: TestingModule = await Test.createTestingModule({
      providers: [
        ExpenseApprovalService,
        { provide: getRepositoryToken(ExpenseRequestEntity), useValue: requestRepo },
        { provide: getRepositoryToken(ExpenseApprovalEntity), useValue: approvalRepo },
        { provide: ExpenseNumberService, useValue: numberService },
        { provide: MailService, useValue: mailService },
      ],
    }).compile();

    service = module.get(ExpenseApprovalService);
  });

  const setupFindOne = (expense: Partial<ExpenseRequestEntity>) => {
    requestRepo.findOne.mockResolvedValue(makeExpense(expense));
    requestRepo.save.mockImplementation((e: any) => Promise.resolve(e));
    approvalRepo.create.mockImplementation((data: any) => data);
    approvalRepo.save.mockResolvedValue({});
  };

  // ── approve
  describe('approve', () => {
    it('지정된 1차 승인자가 PENDING 상태 결의서를 승인하면 APPROVED_L1로 전환', async () => {
      setupFindOne({ exrStatus: 'PENDING', exrApprover1Id: 'approver-1', exrApprover2Id: null });

      const result = await service.approve('exr-1', { comment: '승인합니다' }, mockUser('MANAGER', 'approver-1'), 'ent-1');
      expect(result.exrStatus).toBe('APPROVED');
    });

    it('2차 승인자가 있는 1차 승인 후 APPROVED_L1 상태가 되어야 한다', async () => {
      setupFindOne({
        exrStatus: 'PENDING',
        exrApprover1Id: 'approver-1',
        exrApprover2Id: 'approver-2',
      });

      const result = await service.approve('exr-1', {}, mockUser('MANAGER', 'approver-1'), 'ent-1');
      expect(result.exrStatus).toBe('APPROVED_L1');
    });

    it('MASTER 권한 사용자가 자가 승인 시 바로 APPROVED로 전환', async () => {
      setupFindOne({
        exrStatus: 'PENDING',
        exrApprover1Id: 'master-user',
        exrApprover2Id: null,
      });

      const result = await service.approve('exr-1', {}, mockUser('MASTER', 'master-user'), 'ent-1');
      expect(result.exrStatus).toBe('APPROVED');
    });

    it('지정된 승인자가 아닌 MEMBER가 승인하면 ForbiddenException이 발생해야 한다', async () => {
      setupFindOne({ exrStatus: 'PENDING', exrApprover1Id: 'approver-1' });

      await expect(
        service.approve('exr-1', {}, mockUser('MEMBER', 'other-user'), 'ent-1'),
      ).rejects.toThrow(ForbiddenException);
    });

    it('PENDING/APPROVED_L1 이외 상태에서 승인하면 BadRequestException 발생', async () => {
      setupFindOne({ exrStatus: 'DRAFT' });

      await expect(
        service.approve('exr-1', {}, mockUser('MASTER', 'approver-1'), 'ent-1'),
      ).rejects.toThrow(BadRequestException);
    });
  });

  // ── reject
  describe('reject', () => {
    it('1차 승인자가 PENDING 상태를 반려하면 REJECTED로 전환', async () => {
      setupFindOne({ exrStatus: 'PENDING', exrApprover1Id: 'approver-1' });

      const result = await service.reject('exr-1', { comment: '예산 초과' }, mockUser('MANAGER', 'approver-1'), 'ent-1');
      expect(result.exrStatus).toBe('REJECTED');
    });

    it('승인자가 아닌 사용자가 반려하면 ForbiddenException 발생', async () => {
      setupFindOne({ exrStatus: 'PENDING', exrApprover1Id: 'approver-1' });

      await expect(
        service.reject('exr-1', { comment: '반려' }, mockUser('MEMBER', 'other-user'), 'ent-1'),
      ).rejects.toThrow(ForbiddenException);
    });

    it('PENDING/APPROVED_L1 이외 상태에서 반려하면 BadRequestException 발생', async () => {
      setupFindOne({ exrStatus: 'APPROVED' });

      await expect(
        service.reject('exr-1', { comment: '반려' }, mockUser('MASTER', 'approver-1'), 'ent-1'),
      ).rejects.toThrow(BadRequestException);
    });
  });

  // ── submit
  describe('submit', () => {
    it('DRAFT 상태 결의서를 제출하면 PENDING으로 전환되어야 한다', async () => {
      setupFindOne({ exrStatus: 'DRAFT', exrApprover1Id: 'approver-1', exrRequesterId: 'user-1' });

      const result = await service.submit('exr-1', mockUser('MEMBER', 'user-1'), 'ent-1');
      expect(['PENDING', 'APPROVED']).toContain(result.exrStatus);
    });

    it('작성자가 아닌 사용자가 제출하면 ForbiddenException 발생', async () => {
      setupFindOne({ exrStatus: 'DRAFT', exrRequesterId: 'user-1' });

      await expect(
        service.submit('exr-1', mockUser('MEMBER', 'other-user'), 'ent-1'),
      ).rejects.toThrow(ForbiddenException);
    });

    it('DRAFT/REJECTED 이외 상태에서 제출하면 BadRequestException 발생', async () => {
      setupFindOne({ exrStatus: 'PENDING', exrRequesterId: 'user-1' });

      await expect(
        service.submit('exr-1', mockUser('MEMBER', 'user-1'), 'ent-1'),
      ).rejects.toThrow(BadRequestException);
    });
  });
});

