// 4. ExpenseRecurringService
// ═════════════════════════════════════════════════════════════════════════════
describe('ExpenseRecurringService', () => {
  let service: ExpenseRecurringService;
  let repo: ReturnType<typeof createMockRepository>;
  let itemRepo: ReturnType<typeof createMockRepository>;

  beforeEach(async () => {
    repo = createMockRepository();
    itemRepo = createMockRepository();

    const module: TestingModule = await Test.createTestingModule({
      providers: [
        ExpenseRecurringService,
        { provide: getRepositoryToken(ExpenseRequestEntity), useValue: repo },
        { provide: getRepositoryToken(ExpenseRequestItemEntity), useValue: itemRepo },
      ],
    }).compile();

    service = module.get(ExpenseRecurringService);
  });

  describe('generateDueExpenses', () => {
    it('만기된 정기 결의서로부터 자식 결의서가 생성되어야 한다', async () => {
      const parentExpense = makeExpense({
        exrFrequency: 'RECURRING' as any,
        exrStatus: 'APPROVED',
        exrNextDueDate: '2026-03-01',
        exrRecurringType: 'MONTHLY',
        items: [
          {
            eriId: 'item-1',
            exrId: 'exr-1',
            eriName: '임대비용',
            eriAmount: 500000,
            eriQuantity: 1,
            eriCreatedAt: new Date(),
          } as any,
        ],
      } as any);

      repo.find.mockResolvedValue([parentExpense]);
      repo.create.mockImplementation((data: any) => ({ ...data, exrId: 'exr-child-1' }));
      repo.save.mockImplementation((e: any) => Promise.resolve({ ...e, exrId: e.exrId || 'exr-child-1' }));
      itemRepo.create.mockImplementation((data: any) => data);
      itemRepo.save.mockResolvedValue({});

      const count = await service.generateDueExpenses('2026-03-10');
      expect(count).toBe(1);
      expect(repo.create).toHaveBeenCalledWith(
        expect.objectContaining({
          exrParentId: 'exr-1',
          exrIsAutoGenerated: true,
          exrStatus: 'DRAFT',
          exrFrequency: 'ONE_TIME',
        }),
      );
    });

    it('만기된 정기 결의서가 없으면 0을 반환해야 한다', async () => {
      repo.find.mockResolvedValue([]);

      const count = await service.generateDueExpenses('2026-03-10');
      expect(count).toBe(0);
    });
  });
});
